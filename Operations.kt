// Arithmetic Operators

fun main(){
    val number1 = 45
    val number2 = 5.6
    var result: Double

    result = number1 +number2
    println(result)

    result = number1 - number2
    println(result)

    result = number1 * number2
    println(result)

    result = number1 / number2
    println(result)

    result = number1 % number2
    println(result)

}
