// Assignment operators
fun main(){
    var num1 = 67.3
    val num2 = 25.7

    num1 += num2
    println(num1)

    var num3 = 45.2
    num3 -= num2
    println(num3)

    var num4 = 13.8
    num4 *= num2
    println(num4)

    var num5 = 34.4
    num5 /= num2
    println(num5)

    var num6 = 32.5
    num6 %= num2
    println(num6)



}